   static final Map<String, List<String>> _shuffled = {};
  static final Map<String, int> _index = {};

  static void init() {
    _ideasByCategory.forEach((key, value) {
      _shuffled[key] = List.from(value)..shuffle();
      _index[key] = 0;
    });
  }

  static String getIdea(String category) {
    final cat = category == "Категории"
        ? (_ideasByCategory.keys.toList()..shuffle()).first
        : category;

    final i = _index[cat]!;
    final idea = _shuffled[cat]![i];

    _index[cat] = (i + 1) % _shuffled[cat]!.length;

    if (_index[cat] == 0) {
      _shuffled[cat] = List.from(_ideasByCategory[cat]!)..shuffle();
    }

    return idea;
  }

  static List<String> getCategories() {
    return _ideasByCategory.keys.toList();
  }
